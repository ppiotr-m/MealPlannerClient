package piotr.michalkiewicz.mealplannerclient.model;

public class NutritionInformation {

    Double calories;

    /**
     * The number of grams of carbohydrates.
     */

    Double carbohydrateContent;

    /**
     * The number of milligrams of cholesterol.
     */

    Double cholesterolContent;

    /**
     * The number of grams of fat.
     */

    Double fatContent;

    /**
     * The number of grams of fiber.
     */

    Double fiberContent;

    /**
     * The number of grams of protein.
     */

    Double proteinContent;

    /**
     * The number of grams of saturated fat.
     */

    Double saturatedFatContent;

    /**
     * The serving size, in terms of the number of volume or mass.
     */

    String servingSize;

    /**
     * The number of milligrams of sodium.
     */

    Double sodiumContent;

    /**
     * The number of grams of sugar.
     */

    Double sugarContent;

    /**
     * The number of grams of trans fat.
     */

    Double transFatContent;

    /**
     * The number of grams of unsaturated fat.
     */

    Double unsaturatedFatContent;
}
